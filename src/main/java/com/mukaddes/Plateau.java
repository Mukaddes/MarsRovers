package com.mukaddes;


/**
 * This class represents a plateau on Mars with width and height.
 * @author Mukaddes
 *
 */
public class Plateau {
	
	private int height;
	private int width;
	
	public int getHeight() {
		return height;
	}
	
	public void setHeight(int height) {
		this.height = height;
	}
	
	public int getWidth() {
		return width;
	}
	
	public void setWidth(int width) {
		this.width = width;
	}
	
}
